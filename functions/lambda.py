#ceate a lambda function to compute the cube of a number
cube = lambda x: x**3
print(cube(7))
# The code above defines a lambda function that computes the cube of a number.
# The lambda function cube takes a single argument x and returns x^3.
# The lambda function is assigned to the variable cube.
# The lambda function is called with the argument 7, and the result is printed.
# The cube of 7 is calculated using the lambda function and displayed as output.
# The lambda function cube can be used to compute the cube of any number by passing it as an argument.
